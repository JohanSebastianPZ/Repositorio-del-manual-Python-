print((25 > 10) and 25 == 25) #comprobamos que realmente 25 sea > a 10 y que la variable edad sea 25 (ambas condiciones sean ciertas)
print((25 > 10) or 10 == 5) #comprobamos que una de las dos condiciones sea cierta
print(not 10 > 5) #da igual que la condicion sea true, el not la cambia a la inversa